#include <iostream>
using namespace std;

int fun1(int n){
	return n*n;
}
int fun2(int x,int y){
	if(x>y){return x;}
	else {return y;}	
}
int fun3(int a,int b,int c){
	return (a*b)+c;
}

int main(){
	int x=4, y=7;
	cout<<fun1(x)<<endl;
	cout<<fun1(x+y)<<endl;
	cout<<fun1(y+6)<<endl;
	
	cout<<fun2(x,y)<<endl;
	cout<<fun2(5,-4)<<endl;
	cout<<fun2(y,8)<<endl;

	cout<<fun3(1,5,2)<<endl;
	cout<<fun3(x,y,x)<<endl;
	cout<<fun3(y,4,-4)<<endl;

	return 0;
}
