#include <iostream>
using namespace std;

void subtractFirst(int a[],int b){
	int temp=a[0];
	for(int i=0;i<b;i++){
		cout<<temp<<endl;
		a[i]-=temp;
	}
}
int main() {
	int array[6] = {3,1,4,1,5,9};
	subtractFirst(array, 6);
	for (int i = 0; i < 6; i++)
	cout << array[i] << " "; // Output will be 0 -2 1 -2 2 6
	return 0;
}
